package stringcalculator;

public class Operation {
    public static Number addition(int number1, int number2) {
        return number1 + number2;
    }

    public static Number subtraction(int number1, int number2) {
        return number1 - number2;
    }

    public static Number multiply(int number1, int number2) {
        return number1 * number2;
    }
}
